#include "libft.h"

int	ft_isnumber(char *str)
{
	if (*str == '-' || *str == '+')
		++str;
	while (*str)
	{
		if ((*str) < '0' || (*str) > '9')
			return (0);
		++str;
	}
	if (!*str)
		return (1);
	return (0);
}
